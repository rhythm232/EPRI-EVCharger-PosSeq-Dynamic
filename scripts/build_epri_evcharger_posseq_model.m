build_frac_ss(mdlName,[mdlName '/EVCharger_Fraction_C'], ...
    'EVCharger_Fraction_C',code_C,'PfrC',6,[c4 r1 c4+W r1+H]);

code_D = frac_D_code('EVCharger_Fraction_D', P_D_kW, Ts);
build_frac_ss(mdlName,[mdlName '/EVCharger_Fraction_D'], ...
    'EVCharger_Fraction_D',code_D,'PfrD',4,[c5 r1 c5+W r1+H]);

